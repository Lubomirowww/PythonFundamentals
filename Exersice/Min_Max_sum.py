def Min_Max_Sum_func(nums):
    print(f'Max num is: {max(nums)}')
    print(f'Min num is: {min(nums)}')
    print(f'Sum of nums is: {sum(nums)}')




numbers = list(list(map(int,input().split(' '))))

Min_Max_Sum_func(numbers)


